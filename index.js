let comment = "구독 좋아요";
console.log("🚀 ~ comment:", comment);
// let 은 변수, const 는 상수. 상수는 재할당이 불가, 변수는 가능

console.log("youtube", youtube);
comment = "태요미네";
// 이미 정의되어 있는 구독 좋아요를 태요미네로 재할당하겠다는 뜻.
console.log("🚀 ~ comment:", comment);
// 변경 이후부터 실행된 콘솔로그부터 변경값으로 사용됨. 순차적으로 사용됨.
